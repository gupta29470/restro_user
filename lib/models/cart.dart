import 'package:flutter/foundation.dart';

class CartItem {
  final dynamic itemName;
  final dynamic itemImage;
  final dynamic price;
  final dynamic quantity;
  final dynamic date;
  final dynamic time;

  CartItem({
    @required this.itemName,
    @required this.itemImage,
    @required this.price,
    @required this.quantity,
    @required this.date,
    @required this.time,
  });
}




